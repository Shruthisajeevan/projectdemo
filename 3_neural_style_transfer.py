def run_style_transfer(content_img, style_img, n_steps=400,
                       style_weight=800_000, content_weight=1, tv_weight=0.5):

    model, style_losses, content_losses = build_model(
        cnn, IMAGENET_MEAN, IMAGENET_STD, style_img, content_img
    )

    input_img = content_img.clone().requires_grad_(True)
    optimizer = optim.LBFGS([input_img], max_iter=20, line_search_fn='strong_wolfe')

    run = [0]
    best = [None, float('inf')]

    def closure():
        with torch.no_grad():
            input_img.clamp_(0, 1)
        optimizer.zero_grad()
        model(input_img)

        style_score   = sum(sl.loss for sl in style_losses)   * style_weight
        content_score = sum(cl.loss for cl in content_losses) * content_weight
        tv_loss = (
            torch.pow(input_img[:,:,1:,:] - input_img[:,:,:-1,:], 2).mean() +
            torch.pow(input_img[:,:,:,1:] - input_img[:,:,:,:-1], 2).mean()
        ) * tv_weight

        loss = style_score + content_score + tv_loss
        loss.backward()

        run[0] += 1
        if run[0] % 50 == 0:
            print(f"  Step {run[0]:3d} | Style: {style_score.item():.2f} | "
                  f"Content: {content_score.item():.2f} | TV: {tv_loss.item():.4f}")

        if loss.item() < best[1]:
            best[1] = loss.item()
            best[0] = input_img.data.clone()

        return loss

    for _ in range(n_steps):
        optimizer.step(closure)

    with torch.no_grad():
        input_img.clamp_(0, 1)

    return best[0]

