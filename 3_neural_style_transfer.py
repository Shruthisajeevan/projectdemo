content_layers = ['conv_8']                                        # relu3_2
style_layers   = ['conv_1', 'conv_3', 'conv_5', 'conv_9', 'conv_13']  # 1 per block

def build_model(cnn, norm_mean, norm_std, style_img, content_img):
    model = nn.Sequential(Normalization(norm_mean, norm_std))
    content_losses, style_losses = [], []
    i = 0

    for layer in cnn.children():
        if isinstance(layer, nn.Conv2d):
            i += 1; name = f'conv_{i}'
        elif isinstance(layer, nn.ReLU):
            name = f'relu_{i}'
            layer = nn.ReLU(inplace=False)
        elif isinstance(layer, nn.MaxPool2d):
            name = f'pool_{i}'
            layer = nn.AvgPool2d(2, stride=2)   # smoother gradients
        elif isinstance(layer, nn.BatchNorm2d):
            name = f'bn_{i}'
        else:
            raise RuntimeError(f'Unknown layer: {layer.__class__.__name__}')

        model.add_module(name, layer)

        if name in content_layers:
            cl = ContentLoss(model(content_img).detach())
            model.add_module(f'content_loss_{i}', cl)
            content_losses.append(cl)

        if name in style_layers:
            sl = StyleLoss(model(style_img).detach())
            model.add_module(f'style_loss_{i}', sl)
            style_losses.append(sl)

    # Trim after last loss
    for j in range(len(model)-1, -1, -1):
        if isinstance(model[j], (ContentLoss, StyleLoss)):
            break
    return model[:(j+1)], style_losses, content_losses

